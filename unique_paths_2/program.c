void find_paths(int **obstacleGrid, int x, int y, int *paths, int rows, int cols) {
    if((x == (rows - 1)) && (y == (cols - 1)) && (obstacleGrid[x][y] != 1)) {
        (*paths)++;
    }
    if(obstacleGrid[x][y] == 1) {
        return;
    }
    if(x < (rows - 1)) {
        find_paths(obstacleGrid, x + 1, y, paths, rows, cols);  // going down
    }
    if(y < (cols - 1)) {
        find_paths(obstacleGrid, x, y + 1, paths, rows, cols); // going right
    }
}

